class ArithmeticOperations(var x:Int, var y:Int){
    fun addition():Int{
        return this.x + this.y
    }

    fun subtraction():Int{
        return this.x - this.y
    }

    fun multiplication():Int{
        return this.x * this.y
    }

    fun division():Int{
        return this.x / this.y
    }
}